public class TypeConvEx {
    public static void main(String[] args) {
        /**
         * Type Casting in Java
         * In Java, type casting is a method or process that converts a data type into
         * another data type in both
         * ways manually and automatically. The automatic conversion is done by the
         * compiler and manual conversion
         * performed by the programmer. In this section, we will discuss type casting
         * and its types with proper examples.
         */

        int i = 10000;
        byte s = (byte)i;
        System.out.println(s);
        char c = 'A';
        int value = c;
        System.out.println(value);
    }
}
