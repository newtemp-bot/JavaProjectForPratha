class DatatypeEx
{
	public static void main(String arg[])
	{
		// Syntax for variable initilzation = ( Datatype Variable_name = value; )
		// ( Datatype varibaleName;)

		boolean b = true;
		char c = '$';
		byte by = 127;
		short st = 20;
		int i = 50; 
		long l = 1244;

		float f = 12.5f;
		double d = 12.5;
		
		System.out.println("\nBoolean value is = "+b);
		System.out.println("Char value is = "+c);
		System.out.println("Byte value is = "+by);
		System.out.println("Short value is = "+st); 
		System.out.println("Long value is = "+l);
		System.out.println("int value is = "+i);
		System.out.println("\n\n\nfloat value is = "+f);
		System.out.println("double value is = "+d);

		System.out.println("\n\n\n"+(char)65);
	}
}