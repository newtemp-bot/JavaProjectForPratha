public class AssignOpEx {
    public static void main(String[] args) {
        int a = 10;
        a += 10; //a = a + 10;
        a -= 2; // a = a - 2; 
        //  *=
        //  /=
        //  %=
        //  !=  It use where we need true and false value on certain conditions like (It check value is not equal)
        //  ==  It use where we need true and false value on certain conditions like (It check value is equal)
        System.out.println("\n\n\n"+a);
        System.out.println(10==10);//true
        System.out.println(10==12);//false
        System.out.println(10!=10);//false
        System.out.println(10!=12);//true
        
    }
}